- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
    }
    
    // Configure the cell.
    [cell.textLabel setText:[self getFormatForInt:indexPath.row]];
	cell.textLabel.adjustsFontSizeToFitWidth = YES;
    
    return cell;
}

-(NSString*) getFormatForInt:(int)i
{
    // Configure the cell.
    switch(i)
    {
        case 0: return @"Standard"; break;
        case 1: return @"Extended"; break;
        case 2: return @"Modern"; break;
        case 3: return @"Legacy"; break;
        case 4: return @"Vintage"; break;
        case 5: return @"Commander"; break;
        
        default: return @"ERROR: Unknown format!  Please email me at MTGJudgeBugs@gmail.com to submit a bug report. (Error ID #32)"; break;
    }	
}

